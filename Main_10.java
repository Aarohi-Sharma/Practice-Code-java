public class Main_10 {
    public static void main(String args[]) {
        int i=56;
        int j=67;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        System.out.println(j++*2);
        System.out.println(j);
        System.out.println(++j*2);
        System.out.println(j);
        char ch='a';
        System.out.println(++ch);
        System.out.println(ch++);
    }
}
